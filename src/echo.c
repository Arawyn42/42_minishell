#include "minishell.h"

void	echo(char *line)
{
	int	i;

	i = 5;
	if (line[5] == '-' && line[6] == 'n' && line[7] == ' ')
	{
		i = 8;
		while(line[i])
		{
			write(1, &line[i], 1);
			i++;
		}
	}
	else
	{
		while (line[i])
		{
			write(1, &line[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
